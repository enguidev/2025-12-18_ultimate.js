// PODEMOS REALIZAR OPERACIONES MATEMÁTICAS //

let a = 10;
let b = 8;

console.log(a + b, "adición"); // adición
console.log(a - b, "resta"); // resta
console.log(a * b, "multiplicación"); // multiplicación
console.log(a / b, "división"); // división
console.log(a % b, "módulo"); // módulo
console.log(a ** b, "potencia"); // potencia

// Valor inicial de a
console.log(a, "valor inicial de a");

// incrementar
console.log(++a, "con pre incremento"); // pre incremento
console.log(a++, "con post incremento"); // post incremento
console.log(a, "con el pre incremento y posterior post incremento"); // pre incremento + post incremento

// decrementar
console.log(--a, "con pre decremento"); // pre decremento
console.log(a--, "con post decremento"); // post decremento
console.log(a, "con el pre decremento y posterior post decremento"); // pre incremento + post incremento

//--------------------------PROFE--------------------------//
/*
    Operadores aritméticos
    +   -   *   /   %   ++   --
*/

let num1 = 12;
let num2 = 5;

console.log(`Suma: ${num1 + num2}`);
console.log(`Resta: ${num1 - num2}`);
console.log(`Multiplicación: ${num1 * num2}`);
console.log(`División: ${num1 / num2}`);
console.log(`Módulo: ${num1 % num2}`);
console.log(`Incremento: ${++num1}`); //Incrementa antes de la sentencia
console.log(`Incremento: ${num1--}`); //Decrementa después de la sentencia.
